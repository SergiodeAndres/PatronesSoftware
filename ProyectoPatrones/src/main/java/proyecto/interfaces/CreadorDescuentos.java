    private void cargarTabla(){
        ArrayList<CodigoDescuento> descuentos = proxy.getCodigosDescuentoPorProducto(producto);
        
        for (CodigoDescuento cd:descuentos){
            mt.addRow(new Object[]{cd.getCodigo(), cd.getCantidad()});
        }
    }
    
    private void vaciarTabla(){
        int rows = mt.getRowCount();
        
        for (int i = 0; i < rows; i++){
            mt.removeRow(0);
        }
    }
    
    private String generarCodigo(String numero) {
        String codigo = String.valueOf(LocalDateTime.now());
        codigo = codigo.replace("-", "");
        codigo = codigo.replace(".", "");
        codigo = codigo.replace(":", "");
        codigo =  codigo.replace("T", "");
        codigo += numero;
        return codigo;
    }

