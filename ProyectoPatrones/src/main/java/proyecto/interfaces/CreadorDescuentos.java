    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int cantidad = Integer.parseInt(String.valueOf(jSpinner1.getValue()));
        double descuento = Double.parseDouble(String.valueOf(jSpinner2.getValue()));
        BigDecimal bd = new BigDecimal(descuento);
        bd = bd.setScale(2, RoundingMode.HALF_UP);
        
        for (int i = 0; i < cantidad; i++){
            CodigoDescuento cd = new CodigoDescuento(generarCodigo(String.valueOf(i)), bd.doubleValue(), producto);
            proxy.addCodigoDescuento(cd);
        }
        
        proxy.guardarCodigosDescuento();
        vaciarTabla();
        cargarTabla();
    }//GEN-LAST:event_jButton1ActionPerformed

